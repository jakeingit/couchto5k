package com.jakegosskuehn.couchto5k;

public class runpattern {

    int runday;
    int walklength;
    int runlength;
    int repeations;
    
    public runpattern(int day, int walklength_cstr, int runlength_cstr, int repetions_cstr) {
        runday = day;
        walklength = walklength_cstr;
        runlength = runlength_cstr;
        repeations = repetions_cstr;
    }
}
